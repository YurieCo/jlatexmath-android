package com.dhsdevelopments.androidjlatexmath.swingcompat;

public class Insets
{
    private final int top;
    private final int left;
    private final int bottom;
    private final int right;

    public Insets( int top, int left, int bottom, int right ) {
        this.top = top;
        this.left = left;
        this.bottom = bottom;
        this.right = right;
    }
}
