package com.dhsdevelopments.androidjlatexmath.swingcompat.geom;

public class RoundRectangle2D
{
    public static class Float
    {
        public final float x;
        public final float y;
        public final float w;
        public final float h;
        public final float arcWidth;
        public final float arcHeight;

        public Float( float x, float y, float w, float h, float arcWidth, float arcHeight ) {
            this.x = x;
            this.y = y;
            this.w = w;
            this.h = h;
            this.arcWidth = arcWidth;
            this.arcHeight = arcHeight;
        }
    }
}
